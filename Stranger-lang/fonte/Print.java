class Print {
    public void show(String s) {
        System.out.print(s);
    }

    public void show(double d) {
        System.out.print(d);
    }

    public void showln(String s) {
        System.out.println(s);
    }

    public void showln(double d) {
        System.out.println(d);
    }
}